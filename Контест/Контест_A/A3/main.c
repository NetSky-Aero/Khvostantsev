/*Извлечение бит
На вход программе подается беззнаковое 32-битное целое число N и натуральное число K (1 ≤ K ≤ 31).
Требуется взять K младших битов числа N и вывести полученное таким образом число.

Примеры:
Входные данные: 2 1 Результат работы: 0
Входные данные: 12 3 Результат работы: 4
Входные данные: 3 5 : Результат работы: 3
Входные данные: 1024 3 Результат работы: 0
*/


#include <stdio.h>

unsigned int extract_low_bits(unsigned int N, int K)
{
    unsigned int mask = (1u << K) - 1;
    return N & mask;
}

int main(void)
{
    unsigned int N, K;
    scanf("%u %u", &N, &K);
    printf("%u\n", extract_low_bits(N, K));
    return 0;
}
